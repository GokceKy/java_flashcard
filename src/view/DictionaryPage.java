    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            EngDictionaryDAOImpl dc=new EngDictionaryDAOImpl();
            List<English_Dictionary> dictionaryList = dc.getAll();
            JFrame frame= new JFrame("scrool");
            
             frame.setSize(500, 500);  
        frame.setVisible(true);  
       
  
        // set flow layout for the frame  
        frame.getContentPane().setLayout(new FlowLayout());  
  
        JTextArea textArea = new JTextArea(30, 30);  
        JScrollPane scrollableTextArea = new JScrollPane(textArea);  
        textArea.setText("");
  
        scrollableTextArea.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_ALWAYS);  
        scrollableTextArea.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);  
  
        frame.getContentPane().add(scrollableTextArea);  
            
            
            for (English_Dictionary dictionary : dictionaryList) {
                textArea.append(dictionary.getKelime_eng() + " - " + dictionary.getKelime_tr() + "\n");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DictionaryPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
        // TODO add your handling code here:
        EngDictionaryDAOImpl di = new EngDictionaryDAOImpl();
        String kelime_tr = jTextField1.getText();
        String kelime_eng = jTextField2.getText();
        
        English_Dictionary dictionary = di.getTR(kelime_tr);
        English_Dictionary dictionary2 = di.getENG(kelime_eng);

        if (dictionary != null) {
            JOptionPane.showMessageDialog(null, "English: " + dictionary.getKelime_eng() + "\nTurkish: " + dictionary.getKelime_tr());
        } else if(dictionary2 == null){
            JOptionPane.showMessageDialog(null, "word not found.");
        }

        if (dictionary2 != null) {
            JOptionPane.showMessageDialog(null, "English: " + dictionary2.getKelime_eng() + "\nTurkish: " + dictionary2.getKelime_tr());
        } else if(dictionary == null) {
            JOptionPane.showMessageDialog(null, " word not found.");
        }

    } catch (SQLException ex) {
        Logger.getLogger(DictionaryPage.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

